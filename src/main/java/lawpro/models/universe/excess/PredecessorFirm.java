package lawpro.models.universe.excess;

import com.fasterxml.jackson.annotation.JsonValue;

public class PredecessorFirm {

    private String number;
    private String name;
    private String city;
    private String province;
    private Integer inOperationFrom;
    private Integer inOperationTo;
    private String averageNumberOfLawyers;
    private boolean dissolved;

    public PredecessorFirm() {
    }

    public PredecessorFirm(String number, String name, String city, String province, Integer inOperationFrom, Integer inOperationTo, String averageNumberOfLawyers, boolean dissolved) {
        this.number = number;
        this.name = name;
        this.city = city;
        this.province = province;
        this.inOperationFrom = inOperationFrom;
        this.inOperationTo = inOperationTo;
        this.averageNumberOfLawyers = averageNumberOfLawyers;
        this.dissolved = dissolved;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public Integer getInOperationFrom() {
        return inOperationFrom;
    }

    public void setInOperationFrom(Integer inOperationFrom) {
        this.inOperationFrom = inOperationFrom;
    }

    public Integer getInOperationTo() {
        return inOperationTo;
    }

    public void setInOperationTo(Integer inOperationTo) {
        this.inOperationTo = inOperationTo;
    }

    public String getAverageNumberOfLawyers() {
        return averageNumberOfLawyers;
    }

    public void setAverageNumberOfLawyers(String averageNumberOfLawyers) {
        this.averageNumberOfLawyers = averageNumberOfLawyers;
    }

    public boolean isDissolved() {
        return dissolved;
    }

    public void setDissolved(boolean dissolved) {
        this.dissolved = dissolved;
    }
}
