package lawpro.models.universe.excess;

public class CoverageDetail {

    private String insurerCompanyKey;
    private String insurerCompanyName;
    private Integer limitsPerClaim;
    private Integer limitsAggregate;
    private Integer policyPeriodFrom;
    private Integer policyPeriodTo;
    private String policyLawFirmName;

    public CoverageDetail() {
    }

    public CoverageDetail(String insurerCompanyKey, String insurerCompanyName, Integer limitsPerClaim, Integer limitsAggregate, Integer policyPeriodFrom, Integer policyPeriodTo, String policyLawFirmName) {
        this.insurerCompanyKey = insurerCompanyKey;
        this.insurerCompanyName = insurerCompanyName;
        this.limitsPerClaim = limitsPerClaim;
        this.limitsAggregate = limitsAggregate;
        this.policyPeriodFrom = policyPeriodFrom;
        this.policyPeriodTo = policyPeriodTo;
        this.policyLawFirmName = policyLawFirmName;
    }

    public String getInsurerCompanyKey() {
        return insurerCompanyKey;
    }

    public void setInsurerCompanyKey(String insurerCompanyKey) {
        this.insurerCompanyKey = insurerCompanyKey;
    }

    public String getInsurerCompanyName() {
        return insurerCompanyName;
    }

    public void setInsurerCompanyName(String insurerCompanyName) {
        this.insurerCompanyName = insurerCompanyName;
    }

    public Integer getLimitsPerClaim() {
        return limitsPerClaim;
    }

    public void setLimitsPerClaim(Integer limitsPerClaim) {
        this.limitsPerClaim = limitsPerClaim;
    }

    public Integer getLimitsAggregate() {
        return limitsAggregate;
    }

    public void setLimitsAggregate(Integer limitsAggregate) {
        this.limitsAggregate = limitsAggregate;
    }

    public Integer getPolicyPeriodFrom() {
        return policyPeriodFrom;
    }

    public void setPolicyPeriodFrom(Integer policyPeriodFrom) {
        this.policyPeriodFrom = policyPeriodFrom;
    }

    public Integer getPolicyPeriodTo() {
        return policyPeriodTo;
    }

    public void setPolicyPeriodTo(Integer policyPeriodTo) {
        this.policyPeriodTo = policyPeriodTo;
    }

    public String getPolicyLawFirmName() {
        return policyLawFirmName;
    }

    public void setPolicyLawFirmName(String policyLawFirmName) {
        this.policyLawFirmName = policyLawFirmName;
    }
}
