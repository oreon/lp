package lawpro.models.universe.irop;

public class CoverageLimitOption {

    private String perClaim;
    private String aggregrate;

    public CoverageLimitOption(){
    }

    public CoverageLimitOption(String perClaim, String aggregrate) {
        this.perClaim = perClaim;
        this.aggregrate = aggregrate;
    }

    public String getPerClaim() {
        return perClaim;
    }

    public void setPerClaim(String perClaim) {
        this.perClaim = perClaim;
    }

    public String getAggregrate() {
        return aggregrate;
    }

    public void setAggregrate(String aggregrate) {
        this.aggregrate = aggregrate;
    }
}
