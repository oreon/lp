package lawpro.viewmodels.irop;

public class PoaAppointmentViewModel {

    private String rowId;
    private String clientName;
    private String appointmentMade;
    private Boolean familyMember;
    private String nature;
    private Boolean acting;
    private String durationFrom;
    private String durationTo;
    private Boolean writtenNotice;

    public String getRowId() {
        return rowId;
    }

    public void setRowId(String rowId) {
        this.rowId = rowId;
    }

    public String getClientName() {
        return clientName;
    }

    public void setClientName(String clientName) {
        this.clientName = clientName;
    }

    public String getAppointmentMade() {
        return appointmentMade;
    }

    public void setAppointmentMade(String appointmentMade) {
        this.appointmentMade = appointmentMade;
    }

    public Boolean getFamilyMember() {
        return familyMember;
    }

    public void setFamilyMember(Boolean familyMember) {
        this.familyMember = familyMember;
    }

    public String getNature() {
        return nature;
    }

    public void setNature(String nature) {
        this.nature = nature;
    }

    public Boolean getActing() {
        return acting;
    }

    public void setActing(Boolean acting) {
        this.acting = acting;
    }

    public String getDurationFrom() {
        return durationFrom;
    }

    public void setDurationFrom(String durationFrom) {
        this.durationFrom = durationFrom;
    }

    public String getDurationTo() {
        return durationTo;
    }

    public void setDurationTo(String durationTo) {
        this.durationTo = durationTo;
    }

    public Boolean getWrittenNotice() {
        return writtenNotice;
    }

    public void setWrittenNotice(Boolean writtenNotice) {
        this.writtenNotice = writtenNotice;
    }

}
