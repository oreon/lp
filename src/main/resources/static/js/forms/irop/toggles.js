$(document).ready(function(){

    $('input:radio[name="applyingForProtection"]').change(function(){
        if($(this).val() == 'true') {
            $('.applying-for-protection-info').show();
        } else {
            $('.applying-for-protection-info').hide();
        }
    });

    $('input:radio[name="protectionRespectAttorney"]').change(function(){
        if($(this).val() == 'true') {
            $('.poa-table-info').show();
        } else {
            $('.poa-table-info').hide();
        }
    });

    $('input:radio[name="protectionRespectInter"]').change(function(){
        if($(this).val() == 'true') {
            $('.inter-vevos-table-info').show();
        } else {
            $('.inter-vevos-table-info').hide();
        }
    });

    $('input:radio[name="protectionRespectEstate"]').change(function(){
        if($(this).val() == 'true') {
            $('.estate-trust-table-info').show();
        } else {
            $('.estate-trust-table-info').hide();
        }
    });

    $('input:radio[name="writtenNotice"]').change(function(){
        if($(this).val() == 'true') {
            $('.written-notice-info').show();
        } else {
            $('.written-notice-info').hide();
        }
    });

    $('input:radio[name="thirdPartyPayor"]').change(function(){
        if($(this).val() == 'true') {
            $('.third-party-info').show();
        } else {
            $('.third-party-info').hide();
        }
    });

});